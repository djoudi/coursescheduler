<h1>Course Scheduler</h1>
<p>This web based software is designed to simplify making changes to dates in course calendars. </p>
<p>Do you have a course that remains consistent in its assignments EXCEPT that all the dates must be changed each semester? With this program, once your data has been entered, simply changing the start date of the course changes all other dates in the schedule. </p>
<p>Designed primarily for use at Penn State and their semester periods, this software inserts the Fall and Springs breaks and moves Lessons and Assignment dates as needed when converting. Custom schedules can also be created for summer sessions or other time frames.</p>
<p>Once data entry is completed the finished schedule is displayed as an HTML page in your browser.This page can be saved to your desktop. From there is can be uploaded as a file to your course in Angel. It can be opened in Word, converted to a Word or PDF file and offered as a downloadable document. It can be copied and pasted into your syllabus.</p>
<p>This tool is offered for your convenience. Data entered is stored in a database on the ITS servers. Although all precautions are taken we accept no responsibility or liability for loss of data should that occur.</p>

